// 从 OCR 原文中抽取配料表，避免用大量正则规则维护各种食品包装字段。
export function buildIngredientExtractionPrompt(ocrText: string) {
  return `你是一个食品包装 OCR 文本整理助手。

你的任务是从 OCR 识别出的原始文本中，只提取“配料表/配料/原料/成分”相关内容，供用户确认后继续分析。

要求：
1. 只提取配料表本身，以及紧邻配料表且明确属于致敏提示的信息。
2. 不要提取营养成分表、产品类型、净含量、保质期、生产日期、贮存条件、执行标准、厂家、地址、电话、条形码等非配料表信息。
3. 不要补充 OCR 原文中没有出现的成分。
4. 如果 OCR 文本中没有明确配料表，请返回空字符串。
5. 保留原文中的中文标点、括号、百分比、数字、英文缩写和过敏原提示。
6. 输出必须是合法 JSON，不要输出 Markdown，不要输出额外解释。

请严格按照以下 JSON 格式输出：

{
  "ingredientText": ""
}

OCR 原始文本如下：

${ocrText}`;
}

